    public void limpiar(){
        txtIdTrabajador.setText("");
        txtIdPersona.setText("");
        txtNombre.setText("");
        txtPaterno.setText("");
        txtMaterno.setText("");
        txtEdad.setText("");
        calenderFecha.setDate(null);
        groupGenero.clearSelection();
        groupTipoTrabajador.clearSelection();
        txtCedula.setText("");
        groupEstadoCivil.clearSelection();
        idMaxP();
        idMaxT();
        validar();
    }
    public void validar(){
        if(txtNombre.getText().isEmpty()){
            jlbNombre.setText("*Obligatorio");
        }else{
            jlbNombre.setText("");
        }
        if(txtPaterno.getText().isEmpty()){
            jlbPaterno.setText("*Obligatorio");
        }else{
            jlbPaterno.setText("");
        }
        if(txtMaterno.getText().isEmpty()){
            jlbMaternoo.setText("*Obligatorio");
        }else{
            jlbMaternoo.setText("");
        }
        if(txtEdad.getText().isEmpty()){
            jlbEdad.setText("*Obligatorio");
        }else{
            jlbEdad.setText("");
        }
        if(calenderFecha.getDate()==null){
            jlbFecha.setText("*Obligatorio");
        }else{
            jlbFecha.setText("");
        }
        if(rdMasculino.isSelected() || rdFemenino.isSelected()){
            jlbGenero.setText("");
        }else{
            jlbGenero.setText("*Obligatorio");
        }
        if(rdDocente.isSelected() || rdAdministrativo.isSelected()){
            jlbTipo.setText("");
        }else{
            jlbTipo.setText("*Obligatorio");
        }
        if(txtCedula.getText().isEmpty()){
            jlbCedula.setText("*Obligatorio");
        }else{
            jlbCedula.setText("");
        }
        if(rdsoltero.isSelected() || rdCasado.isSelected()){
            jlbEstado.setText("");
        }else{
            jlbEstado.setText("*Obligatorio");
        }
        if(txtIdTrabajador.getText().isEmpty() || txtNombre.getText().isEmpty() || txtPaterno.getText().isEmpty() || 
                txtEdad.getText().isEmpty() || calenderFecha.getDate()==null || !(rdMasculino.isSelected() || rdFemenino.isSelected()) ||
                !(rdDocente.isSelected() || rdAdministrativo.isSelected()) || txtCedula.getText().isEmpty() || 
                !(rdsoltero.isSelected() || rdCasado.isSelected())){
            btnGuardar.setEnabled(false);
            btnModificar.setEnabled(false);
            btnEliminar.setEnabled(false);
        }else{
            btnGuardar.setEnabled(true);
            btnModificar.setEnabled(true);
            btnEliminar.setEnabled(true);
        }
    }
