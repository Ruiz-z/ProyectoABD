    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        if(txtIdTrabajador.getText().isEmpty() || txtNombre.getText().isEmpty() || txtPaterno.getText().isEmpty()
           || txtMaterno.getText().isEmpty() || txtEdad.getText().isEmpty() || calenderFecha.getDate()==null
            || !(rdMasculino.isSelected() || rdFemenino.isSelected()) || !(rdCasado.isSelected() || rdsoltero.isSelected())
            || !(rdAdministrativo.isSelected() || rdDocente.isSelected())){
            JOptionPane.showMessageDialog(null, "LLENE LOS CAMPOS");
        }else{
            char genero, estado, tipo;
            if(rdMasculino.isSelected()){
                genero='M';
            }else{
                genero='F';
            }
            if(rdAdministrativo.isSelected()){
                tipo='A';
            }else{
                tipo='D';
            }
            if(rdsoltero.isSelected()){
                estado='S';
            }else{
                estado='C';
            }
            Date fecha_Nacimiento=calenderFecha.getDate();
            if(fecha_Nacimiento==null){
                JOptionPane.showMessageDialog(null, "Seleccione una fecha valida");
                return;
            }
            int id_Usuario=UsuarioContexto.getId_Usuario();
            Persona persona = new Persona();
            persona.setId_Persona(Integer.parseInt(txtIdPersona.getText()));
            persona.setNombre_Persona(txtNombre.getText());
            persona.setPaterno_Persona(txtPaterno.getText());
            persona.setMaterno_Persona(txtMaterno.getText());
            persona.setEdad_Persona(Integer.parseInt(txtEdad.getText()));
            persona.setFecha_nacimiento(new java.sql.Date(fecha_Nacimiento.getTime()));
            persona.setGenero_Persona((char)genero);
            persona.setId_Usuario(id_Usuario);
            Trabajador trabajador = new Trabajador();
            trabajador.setId_Trabajador(Integer.parseInt(txtIdTrabajador.getText()));
            trabajador.setTipo_Trabajador((char)tipo);
            trabajador.setCedula(txtCedula.getText());
            trabajador.setEstado_Civil((char)estado);
            trabajador.setId_Persona(Integer.parseInt(txtIdPersona.getText()));
            trabajador.setId_Usuario(id_Usuario);            
            String mensajeP=personabo.modificarPersona(persona);
            String mensajeT=trabajadorbo.modificarTrabajador(trabajador);
            if(mensajeP.contains("PERSONA ACTUALIZADA CORRECTAMENTE") && mensajeT.contains("TRABAJADOR ACTUALIZADO CORRECTAMENTE")){
                JOptionPane.showMessageDialog(null, "TRABAJADOR ACTUALIZADO CORRECTAMENTE");
            }else{
                JOptionPane.showMessageDialog(null, "ERROR AL ACTUALIZAR EL TRABAJADOR");
            }
            limpiar();
            listarPersona();
            
        }
    }//GEN-LAST:event_btnModificarActionPerformed

